            <p>
                I then apply an individual contribution multiplier for each person.
                This value is based on my observations as well as the evaluations by
                your client, any consultants, and your peers.
                The multiplier ranges from .7 to 1.1, but a value above 1 is only used in
                exceptional cases.
                Basically, I do not believe that you should be able to get a better grade
                than the product you produced.
            </p>
            <p>
                A few more details:
                <ul>
                    <li>
                        Process includes whether you are interacting appropriately
                        as a team, with me, and with your client.
                        Are you addressing issues as they arise?
                        It includes professionalism in your dealings with your client and
                        your professor and whether you are usually on time
                        with deliverables or habitually late.
                        It includes your web site and the materials that you produce
                        as steps to produce the other artifacts.
                        I will be giving you 3 process grades: for the requirements phase
                        (7%),
                        the design and implementation phase (8%), and the completion (10%).
                        The reason for that is to recognize that there are different
                        processes and issues during different phases of the project.
                    </li>    
                    <li>
                        The code grade covers function, correctness and readability.
                        The three components are equally important.
                        Have you met the primary requirements?
                        How many bugs was I able to find?
                        I will do a random review of the code that you produce.
                        I expect to be able to understand it.
                        This includes web pages as well as other code that you write.
                    </li>    
                    <li>
                        The documentation covers the formal deliverables:
                        the functional spec, the design document, the user manuals,
                        and the test plan.
                        They are weighted equally.
                        Remember that spelling, grammar, and readability are important;
                        unreadable good content is not sufficient.
                    </li>    
                    <li>
                        For the final presentation, your grade will include both
                        content and style.
                    </li>
                </ul>

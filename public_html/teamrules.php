            <a name="team_behavior" /><h2>Team Behavior</h2>
            <p>
                Team behavior should include such items as means of notifying team members if you
                are going to be late to any meeting and how team members will contact and
                communicate with each other.
                Of particular importance is how members will respond
                to emails (and other forms of communication if appropriate):
                how quickly responses can be expected and what no response means.
                The other most important thing to consider early is how you will recover
                from slippages.
                It is important to think beyond "work harder" and "it won't happen."
                Consider, for example, if you can build in who will be the backup and whether
                you have a way to recognize problems earlier rather than later.
            </p>
            <a name="coding_practices" /><h2>Coding Practices</h2>
            <p>
                Coding practices include style issues, such as formatting and prologues
                and how you will maintain the current state of the system.
                It is fine to refer to an existing set of style standards.
                For examples of documents, you can look at
                <ul>
                    <li><a href="http://blogs.msdn.com/b/brada/archive/2005/01/26/361363.aspx" target="_blank">C# example</a></li>
                    <li><a href="http://www.oracle.com/technetwork/java/codeconv-138413.html" target="_blank">Java example</a></li>
                    <li><a href="http://www.python.org/dev/peps/pep-0008/" target="_blank">Python example</a></li>
                    <li><a href="http://www.caliban.org/ruby/rubyguide.shtml" target="_blank">Ruby example</a></li>
                </ul>
                One item that will not be covered in existing standards, however, is
                what the team practice will be in terms of checking in and out code from
                the repository.
                You should strive for consistency of practice on this.
                The most important questions that need to addressed are how long people
                keep files checked out and what type of testing is completed before
                fiels are checked in.
            </p>

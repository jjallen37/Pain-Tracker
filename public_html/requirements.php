            <a name="Concept" /a><h2>Concept</h2>
            <p>
               The concept document is the layman's description of the project.
               It should capture the motivation of the project and act as an
               anchor point if the team or project starts drifting.
               I should be able to turn to this document any time during the semester
               to determine if you have drifted off-course.
               For this project, I will ask for two versions of the document:
               <ul>
                  <li>a one page summary</li>
                  <li>a tweet</li>
               </ul>
            </p>
            <p>
               The single page summary may be include pictures or screen shots
               if appropriate.
               It may be any format: Microsoft Word, OpenOffice Writer, pdf, or HTML.
               It simply needs to be printable on a single sided 8.5 x 11 sheet.
               The tweet must be under 140 characters.
               Both are to be posted on your web site.
            </p>
<!-- Use Cases -->
            <a name="User_Stories" /a><h2>User Stories</h2>
            <p>
               User stories are 2-3
               sentence descriptions of the tasks that different users will want to accomplish.
               They should be short enough to fit on an index card
               and should be written from the perspective of a user.
               The key aspect of the user story is that it should be the way that the
               client thinks of the problem.
            </p>
            <p>
               There are two styles of user stories: a first person style and
               third person.
               The difference comes at least partially from the fact that the
               client may or may not be a user.
               If a user, he may give the description in first person;
               if not, he is more likely to use third person.
               Following is a simple example in both styles:
            </p>
            <p>
               <b>First person:</b>
               As an employee submitting a personnel change form,
               I want to be able to save the changes that I make
               (in all the previous user stories)
               without submitting them
               so that I can confirm my changes with my spouse.
             </p>
            <p>
               <b>Third person:</b>
               Ernie Employee has just gotten married and needs to change
               the beneficiary, tax status, and payroll deductions that the
               company has for him.
               After completing the arduous process of going through each of
               the sections of the HR website, he is nervous that he may
               have not gotten everything the way that he and his wife agreed.
               He does not want to re-enter the information, but would like
               to confirm the changes and submit them tomorrow.               
            </p>

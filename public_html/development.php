<!-- Architecture -->
            <a name="Arch" /a><h2>Architecture</h2>
            <p>
               The architecture describes the highest level external view of the software system.
               It is fine to use one of the software architecture
               models presented in class or to define your own.
               An architecture description will typically have between 3 and 7 components.
               This should be the highest level of decomposition that you use to reason about the system.
               When you are discussing the implications of a design change or how to divide up work,
               you should be able to easily map that back to the architecture.
               The architecture is to be documented as a picture with a paragraph explaining the model.
               While the architecture may be modeled after one of the standard ones,
               your architecture should translate into the application terms
               (e.g., a repository should be labeled by the data that it captures).
               If you are using a standard model, state so explicitly in the paragraph;
               it is a lot simpler than re-explaining well-known concepts.
            </p>

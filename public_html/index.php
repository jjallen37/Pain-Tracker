<!-- Overview -->
	 <h2>Overview</h2>
	 <p>
	    The goal of this course is to teach the skills
	    necessary for building a software product as a team.
	    The lecture portion of the class will cover the broader picture of software
	    engineering that includes a wide range of software development projects
	    in terms of size, complexity, and criticality.
	 </p> 
	 <p>
	    There will be no written exams;
	    there will be individual assignments given to cover the key
	    concepts of the course that are not well reinforced through the project
	    and to expose you to the literature in the field.
	    There are a lot of new things happening in the field of
	    software engineering that you are not exposed to within the
	    department's curriculum.
	    Each team will present a technology from a provided list or
	    one that they are using that has not been taught in other classes.
	    Teams will give a 30-45 minute presentation to the class.
	 </p> 
	 <p>
	    The essence of the course,
	    however, is the faculty-coached team project.
	    Teams of 2-4 students spend
	    the semester negotiating, estimating, scheduling, specifying, coding,
	    debugging, integrating, documenting and testing a substantial
	    programming product.
	    Each project has a real client that is expecting a completed project.
	    Each document will be submitted to the professor in
	    draft form and will be revised based on comments.
	    In addition, documentation needs to be maintained to reflect changes in the product
	    that is being produced.
	 </p> 
	 <p>
	    Project grades are based on code, documentation,
	    ambition, effort, teamwork, and accomplishment. 
	 </p> 
	 <p>
	    The final exam is a presentation of the end product.
	 </p> 
	 <p> 
	    At the end of the course, each student will have experienced all
	    aspects of a software development project, including

	    <ul> 
	       <li>working with a client to define goals and priorities</li> 
	       <li>designing a system</li> 
	       <li>scheduling and planning a multi-person project</li> 
	       <li>effective communications</li> 
	       <li>running meetings</li> 
	       <li>writing technical documentation</li> 
	       <li>preparing web content</li> 
	       <li>writing and testing code</li> 
	       <li>deploying the system</li> 
	       <li>public presentations</li> 
	    </ul>
	 </p>
